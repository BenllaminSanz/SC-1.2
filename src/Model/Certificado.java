package Model;

public class Certificado {

    private int idCertificado;
    private String nombre_Certificado;
    private String gerente1;
    private String gerente2;
    private String gerente3;

    public int getIdCertificado() {
        return idCertificado;
    }

    public void setIdCertificado(int idCertificado) {
        this.idCertificado = idCertificado;
    }

    public String getNombre_Certificado() {
        return nombre_Certificado;
    }

    public void setNombre_Certificado(String nombre_Certificado) {
        this.nombre_Certificado = nombre_Certificado;
    }

    public String getGerente1() {
        return gerente1;
    }

    public void setGerente1(String gerente1) {
        this.gerente1 = gerente1;
    }

    public String getGerente2() {
        return gerente2;
    }

    public void setGerente2(String gerente2) {
        this.gerente2 = gerente2;
    }

    public String getGerente3() {
        return gerente3;
    }

    public void setGerente3(String gerente3) {
        this.gerente3 = gerente3;
    }
}
